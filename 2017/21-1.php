function output($grid)
{
    $size = sqrt(strlen($grid));
    $pos = 0;
    for ($i = 0; $i < $size; $i++) {
        echo substr($grid, $pos, $size) . "\n";
        $pos += $size;
    }
    echo "\n";
}



