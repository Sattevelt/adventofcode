// Parse rules
$regex = '/^([.#\/0-9]+) => ([.#\/]+)$/';
$rules = [];
foreach (explode("\n", $input) as $line) {
    $matches = [];
    preg_match($regex, $line, $matches);

    $in = str_replace('/', '', $matches[1]);
    $out = str_replace('/', '', $matches[2]);
    $len = strlen($in);
    $num = substr_count($in, '#');
    $size = sqrt($len);

    $offset = $size - 1;
    $pos = 0;
    $newString = '';
    for ($i = 0; $i < $size; $i++) {
        $pos = ($pos + $offset) % $len;
        for ($j = 0; $j < $size; $j++) {
            $newString .= $in[$pos];
            if ($j < $size - 1) {
                $pos = ($pos + $size) % $len;
            }
        }
    }

    $rotated = [
        $in,
        $newString,
        strrev($in),
        strrev($newString)
    ];

    foreach ($rotated as $string) {
        $flipped = '';

        $pos = 0;
        for ($i = 0; $i < $size; $i++) {
            $flipped .= strrev(substr($string, $pos, $size));
            $pos += $size;
        }

        $rules[$size][$num][$string] = $out;
        $rules[$size][$num][$flipped] = $out;
    }
}

